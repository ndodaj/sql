-- Data Definition Language (DDL) commands in SQL are used to define and manage the structure of database objects.

-- deals with descriptions of the database schema and is used to create and modify the structure of database objects in the database.

-- CREATE --> Create database or its objects (table, index, function, views, store procedure, and triggers)
-- DROP   --> Delete objects from the database
-- ALTER  --> Alter the structure of the database
-- TRUNCATE --> Remove all records from a table, including all spaces allocated for the records are removed
-- COMMENT --> Add comments to the data dictionary
-- RENAME  -> Rename an object existing in the database







